public class Ex01parte03 {
    public static void main(String[] args) {
        System.out.println("Contagem dos n\u00FAmeros pares de 0 a 20: ");

        for (int i = 0; i <= 20; i++) {
            if (i % 2 == 0) {
                System.out.println(i + " ");
            }
        }
    }

}
